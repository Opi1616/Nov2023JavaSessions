package NavinAssignment;

public class ASCIIValue {

	public static void main(String[] args) {
		char a = 'h';
		System.out.println((int) a);
		System.out.println("==============");

		char b = 'd';
		int c = 3;
		int d = b + (char) c;
		System.out.println((char) d);
		System.out.println("==============");

		double s = 3.9 * 3.9;
		System.out.println(s);
		System.out.println("==============");
		
	}
}
